class Assignment3{
    
    // Adds all ints in an array and returns sum
    public static int addList(int[] arr){
        int sum = 0;
        for(int i : arr){
            sum += i;
        }
        return sum;
    }

    // Multiplies all ints in an array and returns product
    public static multList(int[] arr){
        int res = 1;
        for(int i : arr){
            res *= i;
        }
        return res;
    }
}